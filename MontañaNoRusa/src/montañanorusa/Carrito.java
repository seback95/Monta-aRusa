package montañanorusa;

public class Carrito {
	private int x;
	private int y;
	private int capAscenso; 
	
	public void setY(int y)
{
	this.y=y;
}
	public void setX(int x)
	{
		this.x=x;
	}
	public int getAsc()
	{
		return this.capAscenso;
	}
	public void setAsc(int asc)
	{
		this.capAscenso=asc;
	}
	public int getY()
	{
		return this.y;
	}
	public int getX()
	{
		return this.x;
	}
}

